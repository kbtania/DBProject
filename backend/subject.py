class Subject:

    def __init__(self, title, speciality, course, amount_of_students, amount_of_groups, amount_of_threads, working_plan,
                 calculation_of_hours):
        self.subject_title = title
        self.speciality = speciality
        self.course = course
        self.amount_of_students = amount_of_students
        self.amount_of_groups = amount_of_groups
        self.amount_of_threads = amount_of_threads
        self.provided_by_working_plan = working_plan
        self.calculation_of_hours = calculation_of_hours
